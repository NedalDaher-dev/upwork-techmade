'use client';
import  { Suspense, lazy, useEffect, useState, useContext } from 'react';
import { useRouter, usePathname } from 'next/navigation'; // Removed space before ','
import { Inter } from 'next/font/google';
import Head from 'next/head';
import './globals.css';
import { ThemeProvider, ThemeContext } from '@/contexts/ThemeContext';

const LazyNavbar = lazy(() => import('@/components/Navbar'));
const LazyFooter = lazy(() => import('@/components/Footer'));
const inter = Inter({ subsets: ['latin'] });

const RootLayout = ({ children }) => {
  const [isClient, setIsClient] = useState(false);
  const [isTheme, setIsTheme] = useState('dark'); // Single quotes and removed space before ','
  const router = useRouter();
  const path = usePathname();
  useEffect(() => {
    if (path === '/') {
      setIsTheme('light');
    } else { // Added spaces before and after 'else'
      setIsTheme('dark');
    }
  }, [path]); // Added space after ','

  useEffect(() => {
    setIsClient(true);
  }, []); // Removed comment and added space after ','

  return (
    <html lang="en">
      <ThemeProvider theme={isTheme}>
        <Head>
          <title>Create Next App</title>
          <meta name="description" content="Generated by create next app" /> {/* Single quotes */}
          <meta name="viewport" content="width=device-width, initial-scale=1" />
          {/* Any other head elements here */}
        </Head>
        <body className={inter.className}>
          {isClient && (
            <Suspense fallback={<div>Loading Navbar...</div>}>
              <LazyNavbar type="dark" />
            </Suspense>
          )}
          {children}
        </body>
        {isClient && (
          <Suspense fallback={<div>Loading Footer...</div>}>
            <LazyFooter />
          </Suspense>
        )}
      </ThemeProvider>
    </html>
  );
};

export default RootLayout;
